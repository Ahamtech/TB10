#ifndef MESSAGE_H
#define MESSAGE_H

#include "peer.h"
#include "messageaction.h"
#include "messagemedia.h"

class Message
{
public:

    enum MessageType {
       typeMessageEmpty = 0x83e5de54,
       typeMessageService = 0x9f8d60bb,
       typeMessage = 0x22eb6aba,
       typeMessageForwarded = 0x5f46804
    };

    Message() :
        m_id(0),
        m_toId(Peer::typePeerUser),
        m_unread(false),
        m_action(MessageAction::typeMessageActionEmpty),
        m_fromId(0),
        m_out(false),
        m_date(0),
        m_media(MessageMedia::typeMessageMediaEmpty),
        m_fwdDate(0),
        m_fwdFromId(0),
        m_message(""),
        m_classType(typeMessageEmpty) {}


    Message(MessageType classType) :
        m_id(0),
        m_toId(Peer::typePeerUser),
        m_unread(false),
        m_action(MessageAction::typeMessageActionEmpty),
        m_fromId(0),
        m_out(false),
        m_date(0),
        m_media(MessageMedia::typeMessageMediaEmpty),
        m_fwdDate(0),
        m_fwdFromId(0),
        m_message(""),
        m_classType(classType) {}

    void setId(qint32 id) {
        m_id = id;
    }
    qint32 id() const {
        return m_id;
    }
    void setFromId(qint32 fromId) {
        m_fromId = fromId;
    }
    qint32 fromId() const {
        return m_fromId;
    }
    void setToId(Peer toId) {
        m_toId = toId;
    }
    Peer toId() const {
        return m_toId;
    }
    void setOut(bool out) {
        m_out = out;
    }
    bool out() const {
        return m_out;
    }
    void setUnread(bool unread) {
        m_unread = unread;
    }
    bool unread() const {
        return m_unread;
    }
    void setDate(qint32 date) {
        m_date = date;
    }
    qint32 date() const {
        return m_date;
    }
    void setAction(MessageAction action) {
        m_action = action;
    }
    MessageAction action() const {
        return m_action;
    }
    void setMessage(const QString & message) {
        m_message = message;
    }
    QString message() const {
        return m_message;
    }
    void setMedia(MessageMedia media) {
        m_media = media;
    }
    MessageMedia media() const {
        return m_media;
    }
    void setFwdFromId(qint32 fwdFromId) {
        m_fwdFromId = fwdFromId;
    }
    qint32 fwdFromId() const {
        return m_fwdFromId;
    }
    void setFwdDate(qint32 fwdDate) {
        m_fwdDate = fwdDate;
    }
    qint32 fwdDate() const {
        return m_fwdDate;
    }
    void setClassType(MessageType classType) {
        m_classType = classType;
    }
    MessageType classType() const {
        return m_classType;
    }

private:
    qint32 m_id;
    Peer m_toId;
    bool m_unread;
    MessageAction m_action;
    qint32 m_fromId;
    bool m_out;
    qint32 m_date;
    MessageMedia m_media;
    qint32 m_fwdDate;
    qint32 m_fwdFromId;
    QString m_message;
    MessageType m_classType;
};
#endif // MESSAGE_H
